/* ðŸ”½ðŸ”½ðŸ”½ðŸ”½ðŸ”½ðŸ”½ðŸ”½ðŸ”½ðŸ”½ðŸ”½ðŸ”½ Improving the robot once again, by only moving
torwards places that need something picked up or deliver. To do that, we`ll need
to use some sort of route finding route.

When searching for a route from A to B, we are interested only in the ones that
start at A. We also donâ€™t care about routes that visit the same place
twiceâ€”those are definitely not the most efficient route anywhere.

We are mostly interested in the shortest route: A good approach would be to
â€œgrowâ€ routes from the starting point, exploring every reachable place that
hasnâ€™t been visited yet, until a route reaches the goal.

Here is a function that does this:

The exploring has to be done in the right orderâ€”the places that were reached
first have to be explored first. We canâ€™t immediately explore a place as soon as
we reach it because that would mean places reached from there would also be
explored immediately
*/
function findRoute(graph, from, to) {
  let work = [{ at: from, route: [] }];
  for (let i = 0; i < work.length; i++) {
    let { at, route } = work[i];
    for (let place of graph[at]) {
      if (place == to) return route.concat(place);
      if (!work.some(w => w.at == place)) {
        work.push({ at: place, route: route.concat(place) });
      }
    }
  }
}

function goalOrientedRobot({ place, parcels }, route) {
  if (route.length == 0) {
    let parcel = parcels[0];
    if (parcel.place != place) {
      route = findRoute(roadGraph, place, parcel.place);
    } else {
      route = findRoute(roadGraph, place, parcel.address);
    }
  }
  return { direction: route[0], memory: route.slice(1) };
}

